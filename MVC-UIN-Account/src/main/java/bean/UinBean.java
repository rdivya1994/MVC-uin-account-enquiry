package bean;

public class UinBean {

	    private static final long serialVersionUID = 1L;
	    
	    private String account;
	    private String uin;
	    private String name;
	    private String age;
	    private String address;
	    private String outstandingloan;
	    private String lastpaymentdate;
	    private String repayperiodinmonths;
	    
	    
	    public String getAccount() {
			return account;
		}
		public void setAccount(String account) {
			this.account = account;
		}
		public String getUin() {
			return uin;
		}
		public void setUin(String uin) {
			this.uin = uin;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAge() {
			return age;
		}
		public void setAge(String age) {
			this.age = age;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public String getOutstandingloan() {
			return outstandingloan;
		}
		public void setOutstandingloan(String outstandingloan) {
			this.outstandingloan = outstandingloan;
		}
		public String getLastpaymentdate() {
			return lastpaymentdate;
		}
		public void setLastpaymentdate(String lastpaymentdate) {
			this.lastpaymentdate = lastpaymentdate;
		}
		public String getRepayperiodinmonths() {
			return repayperiodinmonths;
		}
		public void setRepayperiodinmonths(String repayperiodinmonths) {
			this.repayperiodinmonths = repayperiodinmonths;
		}
		
	    
	   
	

}
